function loadGame(index) {
  exitCreateMode();
  currentGameIndex = parseInt(index);
  const game = hrdGames[currentGameIndex];
  currentGameDefinition = {
    name: game.name,
    blocks: game.blocks.map(block => ({
      shape: block.shape.slice(0),
      position: block.position.slice(0),
      directions: block.directions ? block.directions.slice(0) : undefined,
    })),
  };

  loadGameDefinition(currentGameDefinition);
}

function loadGameDefinition(game) {
  stopAutoPlay();
  isCompleted = false;
  currentGameDefinition = {
    name: game.name,
    blocks: game.blocks.map(block => ({
      shape: block.shape.slice(0),
      position: block.position.slice(0),
      directions: block.directions ? block.directions.slice(0) : undefined,
    })),
  };

  // Clone initial blocks and map position to row/col
  initialBlocks = game.blocks.map(block => ({
    shape: block.shape.slice(0),
    row: block.position[0],
    col: block.position[1]
  }));
  currentBlocksState = JSON.parse(JSON.stringify(initialBlocks));
  currentBlocksState.forEach(block => {
    block.visited = new Set([block.row + ',' + block.col]);
  });
  
  // Solve
  const useCombined = document.getElementById('combined-moves-toggle').checked;
  const result = klotski.solve({
    blocks: game.blocks,
    boardSize: [BOARD_ROWS, BOARD_COLS],
    escapePoint: [3, 1],
    singleMove: !useCombined
  });

  if (result) {
    solutionSteps = klotski.mergeSteps(result);
    const totalMoves = solutionSteps.length > 0 ? solutionSteps[solutionSteps.length - 1].step : 0;
    document.getElementById('min-moves').textContent = totalMoves + ' moves';
  } else {
    solutionSteps = [];
    document.getElementById('min-moves').textContent = 'No solution';
  }

  currentStepIndex = 0;
  isReversing = false;
  playbackMode = "Forward";
  manualMovesCount = 0;
  lastMovedBlockIdx = -1;
  solutionMoveOffset = 0;
  manualMoveHistory = [];
  
  renderBoard();
  updateStatus();
  updateControlButtons();
}

function renderBoard() {
  const board = document.getElementById('game-board');
  board.innerHTML = '';
  renderBoardCells(board);

  currentBlocksState.forEach((block, index) => {
    const div = document.createElement('div');
    div.id = 'block-' + index;
    div.className = 'block';
    div.tabIndex = 0;
    div.setAttribute('role', 'button');
    div.setAttribute('aria-label', getBlockAriaLabel(block));
    
    // Size and position
    div.style.width = (block.shape[1] * CELL_SIZE) + 'px';
    div.style.height = (block.shape[0] * CELL_SIZE) + 'px';
    div.style.left = (block.col * CELL_SIZE) + 'px';
    div.style.top = (block.row * CELL_SIZE) + 'px';

    // Type styling
    if (block.shape[0] === 2 && block.shape[1] === 2) {
      div.classList.add('block-caocao');
    } else if (block.shape[0] === 2 && block.shape[1] === 1) {
      div.classList.add('block-vertical');
    } else if (block.shape[0] === 1 && block.shape[1] === 2) {
      div.classList.add('block-horizontal');
    } else if (block.shape[0] === 1 && block.shape[1] === 1) {
      div.classList.add('block-soldier');
    }

    board.appendChild(div);
  });
}

function renderBoardCells(board) {
  for (let row = 0; row < BOARD_ROWS; row++) {
    for (let col = 0; col < BOARD_COLS; col++) {
      const cell = document.createElement('div');
      cell.className = 'board-cell';
      if (row === 3 && (col === 1 || col === 2)) {
        cell.classList.add('escape-cell');
      }
      if (row === 4 && col === 1) {
        cell.classList.add('exit-cell', 'exit-left');
      }
      if (row === 4 && col === 2) {
        cell.classList.add('exit-cell', 'exit-right');
      }
      cell.style.left = (col * CELL_SIZE) + 'px';
      cell.style.top = (row * CELL_SIZE) + 'px';
      board.appendChild(cell);
    }
  }
}

function getBlockAriaLabel(block) {
  return block.shape[0] + ' by ' + block.shape[1] + ' block';
}

