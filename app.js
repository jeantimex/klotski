function getCreatePlacement(clientX, clientY, shape) {
  const board = document.getElementById('game-board');
  const rect = board.getBoundingClientRect();
  const left = clientX - rect.left - (shape[1] * CELL_SIZE) / 2;
  const top = clientY - rect.top - (shape[0] * CELL_SIZE) / 2;
  const col = Math.round(left / CELL_SIZE);
  const row = Math.round(top / CELL_SIZE);

  if (row < 0 || col < 0 || row + shape[0] > BOARD_ROWS || col + shape[1] > BOARD_COLS) {
    return null;
  }

  return { row: row, col: col };
}

function canPlaceCreateBlock(shape, row, col, ignoreIndex) {
  if (row < 0 || col < 0 || row + shape[0] > BOARD_ROWS || col + shape[1] > BOARD_COLS) {
    return false;
  }

  return createBlocks.every((block, index) => {
    if (index === ignoreIndex) {
      return true;
    }

    return (
      row + shape[0] <= block.row ||
      row >= block.row + block.shape[0] ||
      col + shape[1] <= block.col ||
      col >= block.col + block.shape[1]
    );
  });
}

function syncCreateBlocksToBoard() {
  currentBlocksState = createBlocks.map(block => ({
    shape: block.shape.slice(0),
    row: block.row,
    col: block.col,
    visited: new Set([block.row + ',' + block.col]),
  }));
  renderBoard();
  updateCreateControls();
  scheduleCreateSolvabilityCheck();
  updateControlButtons();
}

function removeCreateBlock(index) {
  createBlocks.splice(index, 1);
  syncCreateBlocksToBoard();
}

function shakeCreateBoard() {
  const board = document.getElementById('game-board');
  board.classList.add('shake');
  setTimeout(() => board.classList.remove('shake'), 300);
}

function updateControlButtons() {
  const prevBtn = document.getElementById('prev-btn');
  const nextBtn = document.getElementById('next-btn');
  const autoBtn = document.getElementById('play-pause-btn');

  if (!prevBtn || !nextBtn || !autoBtn) {
    return;
  }

  if (isCreateMode) {
    prevBtn.disabled = true;
    nextBtn.disabled = true;
    autoBtn.disabled = true;
    return;
  }

  const isAutoPlaying = Boolean(autoPlayInterval);
  const isEndState = isCurrentEndState();
  prevBtn.disabled = isAutoPlaying || !hasPreviousState();
  nextBtn.disabled = isAutoPlaying || isEndState;
  autoBtn.disabled = isEndState;
}

function isCurrentEndState() {
  if (playbackMode === "Manual") {
    const caocao = currentBlocksState[0];
    return Boolean(caocao && caocao.row === 3 && caocao.col === 1);
  }

  return solutionSteps.length > 0 && currentStepIndex === solutionSteps.length;
}

function hasPreviousState() {
  if (playbackMode === "Manual") {
    return manualMoveHistory.length > 0;
  }

  return currentStepIndex > 0 || manualMoveHistory.length > 0;
}

function handleStepClick(e) {
  if (isCreateMode) {
    const blockEl = e.target.closest('.block');
    if (blockEl) {
      e.stopPropagation();
      activateBlockElement(blockEl);
    }
    return;
  }

  if (autoPlayInterval) {
    stopAutoPlay();
    return;
  }

  // Find if we clicked on a block
  const blockEl = e.target.closest('.block');
  if (blockEl) {
    e.stopPropagation();
    activateBlockElement(blockEl);
  }
}

function activateBlockElement(blockEl) {
  const blockIdx = parseInt(blockEl.id.replace('block-', ''));

  if (isCreateMode) {
    removeCreateBlock(blockIdx);
    return;
  }

  handleManualBlockClick(blockIdx);
}

function getCurrentMoveCount() {
  if (playbackMode === "Manual") {
    return manualMovesCount;
  }

  const solutionMoves = currentStepIndex > 0 ? solutionSteps[currentStepIndex - 1].step : 0;
  return solutionMoveOffset + solutionMoves;
}

function getLastPlaybackBlockIdx() {
  if (currentStepIndex === 0 || solutionSteps.length === 0) {
    return -1;
  }

  return solutionSteps[currentStepIndex - 1].blockIdx;
}

function prepareManualMove() {
  if (playbackMode === "Manual") {
    return;
  }

  manualMovesCount = getCurrentMoveCount();
  lastMovedBlockIdx = getLastPlaybackBlockIdx();
}

function applyManualMove(blockIdx, target) {
  const block = currentBlocksState[blockIdx];
  manualMoveHistory.push({
    blockIdx: blockIdx,
    row: block.row,
    col: block.col,
    visited: new Set(block.visited),
    manualMovesCount: manualMovesCount,
    lastMovedBlockIdx: lastMovedBlockIdx,
    playbackMode: playbackMode,
    isReversing: isReversing,
    currentStepIndex: currentStepIndex,
    solutionMoveOffset: solutionMoveOffset,
    solutionSteps: solutionSteps.map(step => ({
      blockIdx: step.blockIdx,
      dirIdx: step.dirIdx,
      step: step.step,
      count: step.count,
    })),
  });

  block.row = target.row;
  block.col = target.col;
  block.visited.add(target.key);

  const useCombined = document.getElementById('combined-moves-toggle').checked;
  if (!useCombined || lastMovedBlockIdx !== blockIdx) {
    manualMovesCount++;
  }
  lastMovedBlockIdx = blockIdx;

  playbackMode = "Manual";
  updateBlockDOM(blockIdx);
  updateStatus();
  updateControlButtons();
  checkManualWin();
}

function undoManualMove() {
  const previous = manualMoveHistory.pop();
  if (!previous) {
    return false;
  }

  const block = currentBlocksState[previous.blockIdx];
  block.row = previous.row;
  block.col = previous.col;
  block.visited = new Set(previous.visited);
  manualMovesCount = previous.manualMovesCount;
  lastMovedBlockIdx = previous.lastMovedBlockIdx;
  playbackMode = previous.playbackMode;
  isReversing = previous.isReversing;
  currentStepIndex = previous.currentStepIndex;
  solutionMoveOffset = previous.solutionMoveOffset;
  solutionSteps = previous.solutionSteps.map(step => ({
    blockIdx: step.blockIdx,
    dirIdx: step.dirIdx,
    step: step.step,
    count: step.count,
  }));

  document.getElementById('board-overlay').classList.add('hidden');
  updateBlockDOM(previous.blockIdx);
  updateStatus();
  updateControlButtons();
  return true;
}

function handleManualBlockClick(blockIdx) {
  stopAutoPlay();
  prepareManualMove();
  
  const block = currentBlocksState[blockIdx];
  if (!block.visited) {
    block.visited = new Set();
  }
  // Ensure current location is added to visited
  block.visited.add(block.row + ',' + block.col);

  // Find all valid adjacent spots (Down, Right, Up, Left)
  const validSpots = [];
  for (let d = 0; d < 4; d++) {
    if (canMoveBlock(blockIdx, d)) {
      const targetRow = block.row + directions[d].y;
      const targetCol = block.col + directions[d].x;
      validSpots.push({
        row: targetRow,
        col: targetCol,
        key: targetRow + ',' + targetCol,
        dirIdx: d
      });
    }
  }

  // Filter spots that are not yet visited
  const unvisitedSpots = validSpots.filter(spot => !block.visited.has(spot.key));

  if (unvisitedSpots.length > 0) {
    // Move to the first unvisited spot
    const target = unvisitedSpots[0];
    applyManualMove(blockIdx, target);
  } else {
    // All available spots are visited -> reset visited flags
    block.visited = new Set([block.row + ',' + block.col]);
    
    // Try search again
    const reSearchSpots = validSpots.filter(spot => !block.visited.has(spot.key));
    if (reSearchSpots.length > 0) {
      const target = reSearchSpots[0];
      applyManualMove(blockIdx, target);
    } else {
      // Completely blocked -> shake warning animation
      const blockEl = document.getElementById('block-' + blockIdx);
      if (blockEl) {
        blockEl.classList.add('shake');
        setTimeout(() => blockEl.classList.remove('shake'), 300);
      }
    }
  }
}

function canMoveBlock(blockIdx, dirIdx) {
  const block = currentBlocksState[blockIdx];
  const shape = block.shape;
  const dir = directions[dirIdx];
  
  // Build board representation on the fly
  const board = Array(5).fill(null).map(() => Array(4).fill(-1));
  currentBlocksState.forEach((b, idx) => {
    for (let r = 0; r < b.shape[0]; r++) {
      for (let c = 0; c < b.shape[1]; c++) {
        board[b.row + r][b.col + c] = idx;
      }
    }
  });

  for (let r = 0; r < shape[0]; r++) {
    for (let c = 0; c < shape[1]; c++) {
      const newRow = block.row + dir.y + r;
      const newCol = block.col + dir.x + c;
      
      // Out of bounds
      if (newRow < 0 || newRow >= 5 || newCol < 0 || newCol >= 4) {
        return false;
      }
      
      // Cell occupied by another block
      if (board[newRow][newCol] !== -1 && board[newRow][newCol] !== blockIdx) {
        return false;
      }
    }
  }
  return true;
}

function checkManualWin() {
  const caocao = currentBlocksState.find(b => b.shape[0] === 2 && b.shape[1] === 2);
  if (caocao && caocao.row === 3 && caocao.col === 1) {
    completeGame('Solved!', `You solved it in ${manualMovesCount} manual moves!`);
  }
}

function solveFromCurrentState() {
  const blocks = currentBlocksState.map(block => ({
    shape: block.shape,
    position: [block.row, block.col]
  }));

  const useCombined = document.getElementById('combined-moves-toggle').checked;
  const result = klotski.solve({
    blocks: blocks,
    boardSize: [5, 4],
    escapePoint: [3, 1],
    singleMove: !useCombined
  });

  if (result && result.length > 0) {
    solutionSteps = klotski.mergeSteps(result);
    currentStepIndex = 0;
    isReversing = false;
    solutionMoveOffset = manualMovesCount;
    playbackMode = "Forward";
    return true;
  }
  return false;
}

function triggerNext() {
  if (isCompleted && isCurrentEndState()) {
    return;
  }

  if (playbackMode === "Manual") {
    if (!solveFromCurrentState()) return;
  }
  if (solutionSteps.length === 0) return;

  if (!isReversing) {
    if (currentStepIndex < solutionSteps.length) {
      applyStepForward(solutionSteps[currentStepIndex]);
      currentStepIndex++;
      updateStatus();
      
      // If we just reached the end
      if (currentStepIndex === solutionSteps.length) {
        isReversing = true;
        // Show completion overlay if not in autoplay
        if (!autoPlayInterval) {
          completeGame();
        }
      }
    }
  } else {
    // Reversing back
    if (currentStepIndex > 0) {
      currentStepIndex--;
      applyStepBackward(solutionSteps[currentStepIndex]);
      updateStatus();
      
      if (currentStepIndex === 0) {
        isReversing = false;
      }
    }
  }
}

function triggerPrev() {
  if (playbackMode === "Manual") {
    undoManualMove();
    return;
  }
  stopAutoPlay();

  if (currentStepIndex > 0) {
    currentStepIndex--;
    applyStepBackward(solutionSteps[currentStepIndex]);
    isReversing = false;
    updateStatus();

  } else {
    undoManualMove();
  }
}

function applyStepForward(step) {
  const blockIdx = step.blockIdx;
  const block = currentBlocksState[blockIdx];
  
  block.row += directions[step.dirIdx].y * step.count;
  block.col += directions[step.dirIdx].x * step.count;
  
  updateBlockDOM(blockIdx);
}

function applyStepBackward(step) {
  const blockIdx = step.blockIdx;
  const block = currentBlocksState[blockIdx];
  
  block.row -= directions[step.dirIdx].y * step.count;
  block.col -= directions[step.dirIdx].x * step.count;
  
  updateBlockDOM(blockIdx);
}

function updateBlockDOM(blockIdx) {
  const block = currentBlocksState[blockIdx];
  const div = document.getElementById('block-' + blockIdx);
  if (div) {
    div.style.left = (block.col * CELL_SIZE) + 'px';
    div.style.top = (block.row * CELL_SIZE) + 'px';
  }
}

function updateStatus() {
  const isManual = playbackMode === "Manual";

  if (!isCurrentEndState()) {
    isCompleted = false;
  }

  if (isManual) {
    document.getElementById('step-counter').textContent = manualMovesCount + ' moves';
  } else {
    document.getElementById('step-counter').textContent = getCurrentMoveCount() + ' moves';
  }



  // Hide overlay if we move away from the end state or solve manual win
  if (!isManual && currentStepIndex < solutionSteps.length) {
    document.getElementById('board-overlay').classList.add('hidden');
  }

  // Highlight next block to move
  updateHighlights();
  updateControlButtons();
}

function updateHighlights() {
  // Clear existing highlights
  const blocks = document.querySelectorAll('.block');
  blocks.forEach(b => b.classList.remove('next-up'));

  if (solutionSteps.length === 0 || autoPlayInterval || playbackMode === "Manual" || (currentStepIndex === 0 && !isReversing)) return;

  let targetIdx = -1;
  if (!isReversing) {
    if (currentStepIndex < solutionSteps.length) {
      targetIdx = solutionSteps[currentStepIndex].blockIdx;
    }
  } else {
    if (currentStepIndex > 0) {
      targetIdx = solutionSteps[currentStepIndex - 1].blockIdx;
    }
  }

  if (targetIdx !== -1) {
    const targetEl = document.getElementById('block-' + targetIdx);
    if (targetEl) {
      targetEl.classList.add('next-up');
    }
  }
}

function toggleAutoPlay() {
  if (isCompleted && isCurrentEndState()) {
    return;
  }

  if (autoPlayInterval) {
    stopAutoPlay();
  } else {
    startAutoPlay();
  }
}

function startAutoPlay() {
  if (isCompleted && isCurrentEndState()) {
    return;
  }

  if (playbackMode === "Manual") {
    if (!solveFromCurrentState()) return;
  }
  if (solutionSteps.length === 0) return;

  document.getElementById('board-overlay').classList.add('hidden');
  document.getElementById('play-icon').classList.add('hidden');
  document.getElementById('pause-icon').classList.remove('hidden');
  
  // Clear highlights while autoplaying to avoid distraction
  updateHighlights();

  const autoPlayReversing = isReversing;

  autoPlayInterval = setInterval(() => {
    // If we are reversing and reached 0, stop autoplay
    if (autoPlayReversing && currentStepIndex === 0) {
      stopAutoPlay();
      return;
    }
    // If we are playing forward and reached the end, stop autoplay
    if (!autoPlayReversing && currentStepIndex === solutionSteps.length) {
      completeGame();
      return;
    }

    // Execute next step
    triggerNext();

    // Check if we just reached the end / start after triggerNext
    if (autoPlayReversing && currentStepIndex === 0) {
      stopAutoPlay();
    } else if (!autoPlayReversing && currentStepIndex === solutionSteps.length) {
      completeGame();
    }
  }, 400); // 400ms per step feels extremely satisfying and smooth
  updateControlButtons();
}

function stopAutoPlay() {
  if (autoPlayInterval) {
    clearInterval(autoPlayInterval);
    autoPlayInterval = null;
  }
  document.getElementById('play-icon').classList.remove('hidden');
  document.getElementById('pause-icon').classList.add('hidden');
  updateControlButtons();
  updateHighlights();
}

function completeGame(title = 'Completed!', subtitle = 'Reset the board or choose a new game') {
  isCompleted = true;
  stopAutoPlay();

  // Highlight the Cao Cao block briefly before showing completion
  const caocaoEl = document.getElementById('block-0');
  if (caocaoEl) {
    caocaoEl.classList.add('completed');
  }

  // Wait a moment to let the player see the final position
  setTimeout(() => {
    if (caocaoEl) {
      caocaoEl.classList.remove('completed');
    }
    document.getElementById('board-overlay').classList.remove('hidden');
    document.querySelector('.overlay-title').textContent = title;
    document.querySelector('.overlay-subtitle').textContent = subtitle;
    updateControlButtons();

    // Fire confetti when showing the completed message
    fireCompletionConfetti();
  }, 800);
}

function fireCompletionConfetti() {
  if (typeof confetti !== 'function') {
    return;
  }

  var duration = 2500;
  var end = Date.now() + duration;
  var colors = ['#ff416c', '#ff4b2b', '#ffd700', '#8e54e9', '#00c6ff'];

  (function frame() {
    confetti({
      particleCount: 5,
      angle: 90,
      spread: 160,
      origin: { x: Math.random(), y: -0.1 },
      colors: colors,
      ticks: 200,
      gravity: 1.5,
      drift: Math.random() - 0.5,
      scalar: 1.2
    });

    if (Date.now() < end) {
      requestAnimationFrame(frame);
    }
  })();
}
